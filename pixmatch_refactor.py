#Esta funci√≥n devuelve un emoji basado en el puntaje del jugador, indicando su estado emocional seg√∫n el puntaje.
def ScoreEmoji():
    #puntaje es cero
    if mystate.myscore == 0: return 'üòê'
    #puntaje est√° entre -5 y -1
    elif -5 <= mystate.myscore <= -1: return 'üòè'
    #puntaje est√° entre -10 y -6
    elif -10 <= mystate.myscore <= -6: return '‚òπÔ∏è'
    #puntaje es menor o igual a -11
    elif mystate.myscore <= -11: return 'üòñ'
    #puntaje est√° entre 1 y 5
    elif 1 <= mystate.myscore <= 5: return 'üôÇ'
    #puntaje est√° entre 6 y 10
    elif 6 <= mystate.myscore <= 10: return 'üòä'
    #puntaje es mayor que 10
    elif mystate.myscore > 10: return 'üòÅ'


